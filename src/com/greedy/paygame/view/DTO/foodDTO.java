package com.greedy.paygame.view.DTO;

public class foodDTO {
	
	private String pimang;
	private String eggroll;
	private String kimchi;
	private String sausage;
	private String gogi;
	private String choco;
	private String carrot;
	private String bacon;
	private String nato;
	private String chicken;
	private String straw;

	public foodDTO() {}

	public foodDTO(String pimang, String eggroll, String kimchi, String sausage, String gogi, String choco,
			String carrot, String bacon, String nato, String chicken, String straw) {
		super();
		this.pimang = pimang;
		this.eggroll = eggroll;
		this.kimchi = kimchi;
		this.sausage = sausage;
		this.gogi = gogi;
		this.choco = choco;
		this.carrot = carrot;
		this.bacon = bacon;
		this.nato = nato;
		this.chicken = chicken;
		this.straw = straw;
	
	
	}

	public String getPimang() {
		return pimang;
	}

	public void setPimang(String pimang) {
		this.pimang = pimang;
	}

	public String getEggroll() {
		return eggroll;
	}

	public void setEggroll(String eggroll) {
		this.eggroll = eggroll;
	}

	public String getKimchi() {
		return kimchi;
	}

	public void setKimchi(String kimchi) {
		this.kimchi = kimchi;
	}

	public String getSausage() {
		return sausage;
	}

	public void setSausage(String sausage) {
		this.sausage = sausage;
	}

	public String getGogi() {
		return gogi;
	}

	public void setGogi(String gogi) {
		this.gogi = gogi;
	}

	public String getChoco() {
		return choco;
	}

	public void setChoco(String choco) {
		this.choco = choco;
	}

	public String getCarrot() {
		return carrot;
	}

	public void setCarrot(String carrot) {
		this.carrot = carrot;
	}

	public String getBacon() {
		return bacon;
	}

	public void setBacon(String bacon) {
		this.bacon = bacon;
	}

	public String getNato() {
		return nato;
	}

	public void setNato(String nato) {
		this.nato = nato;
	}

	public String getChicken() {
		return chicken;
	}

	public void setChicken(String chicken) {
		this.chicken = chicken;
	}

	public String getStraw() {
		return straw;
	}

	public void setStraw(String straw) {
		this.straw = straw;
	}
}